package day02;

public class EscapeString {
    public static void main(String[] args) {
        // 제어문자
        //  \n : 줄바꿈
        System.out.println("안녕\n하세요");
        //  \t : tab
        System.out.println("반갑\t습니다.");
        //  \" : "
        //  \' : '
        System.out.println("나는 \"안녕\"이라고 말했다.");
        //  \\ : \
        System.out.println("제어문자는 \\로 시작한다.");
    }
}
